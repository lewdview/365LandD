def create_directories():
    """Create phase directories"""
    for phase in SONGS.keys():
        phase_dir = OUTPUT_BASE / phase
        phase_dir.mkdir(parents=True, exist_ok=True)
        print(f"‚úì Created: {phase_dir}")

def create_placeholder_files():
    """Create placeholder files for ERROR entries"""
    for phase, tracks in SONGS.items():
        phase_dir = OUTPUT_BASE / phase
        
        for day, song_name in tracks:
            if "LOG_" in song_name and "[" in song_name:
                # Format: NN - ERROR_TEXT [LOG_XXX].txt
                target_name = f"{day:02d} - {song_name}.txt"
                target_path = phase_dir / target_name
                target_path.write_text(f"// {song_name}\n// This is a placeholder for LOG entry\n")
                print(f"  ‚úì Placeholder: {target_name}")

